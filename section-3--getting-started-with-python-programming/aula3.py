"""
Python = Linguagem de programação
Tipo de tipagem = Dinamica / Forte
str -> string -> texto
String são texto que estão dentro de aspas
"""

print(1234)

# Aspas simples
print('Luiz Otávio')
print('Luiz "Otávio"')

# Aspas duplas
print("Luiz Otávio")
print("Luiz 'Otávio'")

# Escape
print("Luiz \"Otávio\"")

# r
print(r"Luiz \"Otávio\"")